public class TurneryAndSwitch {
    public static void main(String[] args) {
        int n = 3;
        switch (n) {
            case 1:            
                System.out.println("One");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("Not match");
        }

    }
}
