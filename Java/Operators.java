public class Operators {
    public static void main(String[] args) {
        int m = 9, n = 4;
        int a1 = m + n;
        int a2 = m - n;
        int a3 = m * n;
        double a4 = (double) m / n;
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
        System.out.println(a4);
    }
}
